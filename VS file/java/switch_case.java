 class switch_case
 {
    public static void main(String a[])
    {
        int n=4;
        switch(n){
            case 1:
                System.out.println("monday");break;
            case 2:
                 System.out.println("tuesday");break;
            case 3:
                 System.out.println("wednesday");break;
            case 4:
                 System.out.println("thursday");
                 break;
            case 5:
                 System.out.println("friday");
                 break;     
        }
    }
 }